window.addEventListener('load', async () => {
    if (window.ethereum) {
        web3 = new Web3(window.ethereum);
        await window.ethereum.request({ method: 'eth_requestAccounts' });
        contract = new web3.eth.Contract(abi, contractAddress);
    } else {
        alert("Please install MetaMask!");
    }
});

async function createAuction() {
    const nft = document.getElementById("nftAddress").value;
    const tokenId = document.getElementById("tokenId").value;
    const duration = document.getElementById("duration").value;
    const accounts = await web3.eth.getAccounts();

    await contract.methods.createAuction(nft, tokenId, duration).send({ from: accounts[0] });
    alert("Auction Created!");
}

async function placeBid() {
    const nft = document.getElementById("bidNftAddress").value;
    const tokenId = document.getElementById("bidTokenId").value;
    const bidAmount = web3.utils.toWei(document.getElementById("bidAmount").value, "ether");
    const accounts = await web3.eth.getAccounts();

    await contract.methods.placeBid(nft, tokenId).send({ from: accounts[0], value: bidAmount });
    alert("Bid Placed!");
}

async function withdrawFunds() {
    const accounts = await web3.eth.getAccounts();
    await contract.methods.withdraw().send({ from: accounts[0] });
    alert("Funds Withdrawn!");
}

async function endAuction() {
    const nft = document.getElementById("endNftAddress").value;
    const tokenId = document.getElementById("endTokenId").value;
    const accounts = await web3.eth.getAccounts();

    await contract.methods.endAuction(nft, tokenId).send({ from: accounts[0] });
    alert("Auction Ended!");
}
